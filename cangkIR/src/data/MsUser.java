package data;

import apps.DatabaseConnector;

public class MsUser {
	public static String loggedInUsername;
	public String username = DatabaseConnector.getUsername(loggedInUsername);
	public String userId = DatabaseConnector.getUserID(loggedInUsername);
	public String email;
	public String password;
	public String gender;
	public String role = DatabaseConnector.getUserRole(username);
	
	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}
	
	
	
}
